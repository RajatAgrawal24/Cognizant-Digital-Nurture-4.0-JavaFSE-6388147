CREATE OR REPLACE PROCEDURE ProcessMonthlyInterest IS
  newBalance NUMBER;
BEGIN
  FOR acc IN (
    SELECT AccountID, Balance
    FROM Accounts
    WHERE AccountType = 'Savings'
  ) LOOP
    newBalance := acc.Balance + (acc.Balance * 0.01);

    UPDATE Accounts
    SET Balance = newBalance,
        LastModified = SYSDATE
    WHERE AccountID = acc.AccountID;

    DBMS_OUTPUT.PUT_LINE('Account ID ' || acc.AccountID || ' updated: Old Balance = ' || acc.Balance || ', New Balance = ' || newBalance);
  END LOOP;

  COMMIT;
END;
